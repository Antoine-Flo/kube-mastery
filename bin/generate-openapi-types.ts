import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import openapiTS, { astToString } from 'openapi-typescript'
import YAML from 'yaml'
import type { ClusterResourcesManifest } from '../src/core/cluster/validateClusterResourcesManifest'
import { getOpenAPISchemaName } from '../src/core/openapi/schemaName'

type OpenAPISpecSource = {
  openapi?: string
  info?: {
    title?: string
    version?: string
  }
  components?: {
    schemas?: Record<string, unknown>
  }
}

type MergedOpenAPISpec = {
  openapi: string
  info: {
    title: string
    version: string
  }
  components: {
    schemas: Record<string, unknown>
  }
}

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const workspaceRoot = path.resolve(__dirname, '..')
const specsDir = path.join(workspaceRoot, 'src/core/openapi/specs')
const outFile = path.join(
  workspaceRoot,
  'src/core/openapi/generated/openapi-types.generated.ts'
)
const aliasesOutFile = path.join(
  workspaceRoot,
  'src/core/openapi/generated/k8sOpenapiAliases.generated.ts'
)
const clusterManifestPath = path.join(
  workspaceRoot,
  'src/core/cluster/clusterResources.manifest.yaml'
)

const GENERATED_HEADER =
  '// Code generated by bin/generate-openapi-types.ts. DO NOT EDIT.\n\n'

const buildAliasesFile = (
  merged: MergedOpenAPISpec,
  manifest: ClusterResourcesManifest
): string => {
  const availableSchemas = new Set(Object.keys(merged.components.schemas))
  const mappedResources = manifest.resources
    .map((resource) => {
      const schemaName = getOpenAPISchemaName(resource.apiVersion, resource.kind)
      if (!availableSchemas.has(schemaName)) {
        return null
      }
      return {
        kind: resource.kind,
        apiVersion: resource.apiVersion,
        schemaName
      }
    })
    .filter((resource) => resource !== null)

  const resourceAliasLines = mappedResources
    .map((resource) => {
      return `export type K8s${resource.kind} = RuntimeResource<
  OpenAPIType<'${resource.schemaName}'>,
  '${resource.apiVersion}',
  '${resource.kind}',
  'spec' | 'status'
>`
    })
    .join('\n\n')

  const nestedAliasLines = mappedResources
    .flatMap((resource) => {
      return [
        `export type K8s${resource.kind}Metadata = NonNullable<K8s${resource.kind}['metadata']>`,
        `export type K8s${resource.kind}Spec = K8s${resource.kind} extends { spec?: unknown }\n  ? NonNullable<K8s${resource.kind}['spec']>\n  : never`,
        `export type K8s${resource.kind}Status = K8s${resource.kind} extends { status?: unknown }\n  ? NonNullable<K8s${resource.kind}['status']>\n  : never`
      ]
    })
    .join('\n\n')

  const resourceMapLines = mappedResources
    .map((resource) => {
      return `  ${resource.kind}: K8s${resource.kind}`
    })
    .join('\n')

  return `${GENERATED_HEADER}import type { components } from './openapi-types.generated'

type OpenAPISchemaMap = components['schemas']
type OpenAPISchemaName = keyof OpenAPISchemaMap
type OpenAPIType<TSchemaName extends OpenAPISchemaName> =
  OpenAPISchemaMap[TSchemaName]

type ObjectMetaOpenAPI =
  OpenAPIType<'io.k8s.apimachinery.pkg.apis.meta.v1.ObjectMeta'>

export type RuntimeObjectMeta = Pick<
  ObjectMetaOpenAPI,
  | 'name'
  | 'namespace'
  | 'labels'
  | 'annotations'
  | 'creationTimestamp'
  | 'generation'
  | 'ownerReferences'
  | 'resourceVersion'
  | 'uid'
  | 'deletionTimestamp'
  | 'deletionGracePeriodSeconds'
> & {
  name: string
  namespace: string
  creationTimestamp: string
}

type OpenAPIResourceBase = {
  apiVersion?: string
  kind?: string
  metadata?: ObjectMetaOpenAPI
}

export type RuntimeResource<
  TOpenAPIResource extends OpenAPIResourceBase,
  TApiVersion extends string,
  TKind extends string,
  TRequiredKeys extends PropertyKey = never
> = EnsureRequiredKeys<
  Omit<TOpenAPIResource, 'apiVersion' | 'kind' | 'metadata'> & {
  apiVersion: TApiVersion
  kind: TKind
  metadata: RuntimeObjectMeta
},
  TRequiredKeys
>

type EnsureRequiredKeys<TValue, TKeys extends PropertyKey> = TValue & {
  [TKey in Extract<TKeys, keyof TValue>]-?: NonNullable<TValue[TKey]>
}

${resourceAliasLines}

${nestedAliasLines}

export type K8sResourceByKind = {
${resourceMapLines}
}
`
}

const buildMergedOpenAPIDocument = (): MergedOpenAPISpec => {
  const schemas: Record<string, unknown> = {}
  const specFiles = fs
    .readdirSync(specsDir)
    .filter((fileName) => fileName.endsWith('.json'))
    .sort()

  for (const specFile of specFiles) {
    const specPath = path.join(specsDir, specFile)
    const raw = fs.readFileSync(specPath, 'utf8')
    const parsed = JSON.parse(raw) as OpenAPISpecSource
    const currentSchemas = parsed.components?.schemas ?? {}
    for (const [schemaName, schemaValue] of Object.entries(currentSchemas)) {
      schemas[schemaName] = schemaValue
    }
  }

  return {
    openapi: '3.0.3',
    info: {
      title: 'KubeMastery Bundled OpenAPI',
      version: 'v1.35.3'
    },
    components: {
      schemas
    }
  }
}

const main = async (): Promise<void> => {
  const merged = buildMergedOpenAPIDocument()
  const manifestRaw = fs.readFileSync(clusterManifestPath, 'utf8')
  const manifest = YAML.parse(manifestRaw) as ClusterResourcesManifest
  const ast = await openapiTS(merged as any)
  const rendered = astToString(ast)
  const aliasesRendered = buildAliasesFile(merged, manifest)
  const outputDir = path.dirname(outFile)
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true })
  }
  fs.writeFileSync(outFile, `${GENERATED_HEADER}${rendered}`, 'utf8')
  fs.writeFileSync(aliasesOutFile, aliasesRendered, 'utf8')
  console.log(`Wrote ${path.relative(workspaceRoot, outFile)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, aliasesOutFile)}`)
}

await main()

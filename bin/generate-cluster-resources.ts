const buildClusterEventTypesFile = (
  manifest: ClusterResourcesManifest
): string => {
  const interfaceBlocks = manifest.resources
    .map((resource) => {
      const namespaced = isNamespacedFacade(resource.facade)
      const payloadKey = toEventPayloadKeyFromKind(resource.kind)
      const deletedPayloadKey = `deleted${resource.kind}`
      const previousPayloadKey = `previous${resource.kind}`
      const resourceType = `ClusterResourceTypeByKind['${resource.kind}']`
      const deletedNamespaceLine = namespaced ? '\n    namespace: string' : ''
      const updatedNamespaceLine = namespaced ? '\n    namespace: string' : ''
      return `export interface ${resource.kind}CreatedEvent extends BaseEvent {
  type: '${resource.kind}Created'
  payload: {
    ${payloadKey}: ${resourceType}
  }
}

export interface ${resource.kind}DeletedEvent extends BaseEvent {
  type: '${resource.kind}Deleted'
  payload: {
    name: string${deletedNamespaceLine}
    ${deletedPayloadKey}: ${resourceType}
  }
}

export interface ${resource.kind}UpdatedEvent extends BaseEvent {
  type: '${resource.kind}Updated'
  payload: {
    name: string${updatedNamespaceLine}
    ${payloadKey}: ${resourceType}
    ${previousPayloadKey}: ${resourceType}
  }
}`
    })
    .join('\n\n')

  const unionLines = manifest.resources
    .flatMap((resource) => {
      return [
        `  | ${resource.kind}CreatedEvent`,
        `  | ${resource.kind}DeletedEvent`,
        `  | ${resource.kind}UpdatedEvent`
      ]
    })
    .join('\n')

  const factoryBlocks = manifest.resources
    .map((resource) => {
      const namespaced = isNamespacedFacade(resource.facade)
      const payloadKey = toEventPayloadKeyFromKind(resource.kind)
      const deletedPayloadKey = `deleted${resource.kind}`
      const previousPayloadKey = `previous${resource.kind}`
      const resourceType = `ClusterResourceTypeByKind['${resource.kind}']`
      const deletedNamespaceArg = namespaced ? 'namespace: string,\n  ' : ''
      const deletedNamespacePayload = namespaced ? 'namespace, ' : ''
      const updatedNamespaceArg = namespaced ? 'namespace: string,\n  ' : ''
      const updatedNamespacePayload = namespaced ? 'namespace, ' : ''
      return `export const create${resource.kind}CreatedEvent = (
  ${payloadKey}: ${resourceType},
  source?: string
): ${resource.kind}CreatedEvent => ({
  type: '${resource.kind}Created',
  timestamp: createClusterEventTimestamp(),
  metadata: createClusterEventMetadata(source),
  payload: { ${payloadKey} }
})

export const create${resource.kind}DeletedEvent = (
  name: string,
  ${deletedNamespaceArg}${deletedPayloadKey}: ${resourceType},
  source?: string
): ${resource.kind}DeletedEvent => ({
  type: '${resource.kind}Deleted',
  timestamp: createClusterEventTimestamp(),
  metadata: createClusterEventMetadata(source),
  payload: { name, ${deletedNamespacePayload}${deletedPayloadKey} }
})

export const create${resource.kind}UpdatedEvent = (
  name: string,
  ${updatedNamespaceArg}${payloadKey}: ${resourceType},
  ${previousPayloadKey}: ${resourceType},
  source?: string
): ${resource.kind}UpdatedEvent => ({
  type: '${resource.kind}Updated',
  timestamp: createClusterEventTimestamp(),
  metadata: createClusterEventMetadata(source),
  payload: { name, ${updatedNamespacePayload}${payloadKey}, ${previousPayloadKey} }
})`
    })
    .join('\n\n')

  return `${GENERATED_HEADER}import type { BaseEvent } from '../../events/types'
import type { ClusterResourceTypeByKind } from '../generated/clusterResourceModels.generated'

${interfaceBlocks}

export type GeneratedClusterCrudEvent =
${unionLines}

export const createClusterEventMetadata = (
  source?: string
): BaseEvent['metadata'] => ({
  source: source || 'cluster',
  correlationId: crypto.randomUUID()
})

export const createClusterEventTimestamp = (): string => new Date().toISOString()

${factoryBlocks}
`
}

const buildClusterEventWatchMetaFile = (
  manifest: ClusterResourcesManifest
): string => {
  const resourceBlocks = manifest.resources
    .map((resource) => {
      const resourceKey = resource.collectionKey.toLowerCase()
      const kind = resource.kind
      const payloadKey = toEventPayloadKeyFromKind(kind)
      const deletedPayloadKey = `deleted${kind}`
      const namespaced = isNamespacedFacade(resource.facade)
      // Core v1 Event uses metadata without labels in the simulation model.
      const labelsExprForLiveObject =
        kind === 'Event'
          ? 'undefined'
          : `event.payload.${payloadKey}.metadata.labels`
      const labelsExprForDeletedObject =
        kind === 'Event'
          ? 'undefined'
          : `event.payload.${deletedPayloadKey}.metadata.labels`

      const createdReturn = namespaced
        ? `      return {
        name: event.payload.${payloadKey}.metadata.name,
        namespace: event.payload.${payloadKey}.metadata.namespace,
        labels: ${labelsExprForLiveObject}
      }`
        : `      return {
        name: event.payload.${payloadKey}.metadata.name,
        namespace: '',
        labels: ${labelsExprForLiveObject}
      }`

      const updatedReturn = namespaced
        ? `      return {
        name: event.payload.name,
        namespace: event.payload.namespace,
        labels: ${labelsExprForLiveObject}
      }`
        : `      return {
        name: event.payload.name,
        namespace: '',
        labels: ${labelsExprForLiveObject}
      }`

      const deletedReturn = namespaced
        ? `      return {
        name: event.payload.name,
        namespace: event.payload.namespace,
        labels: ${labelsExprForDeletedObject}
      }`
        : `      return {
        name: event.payload.name,
        namespace: '',
        labels: ${labelsExprForDeletedObject}
      }`

      return `  if (parsedResource === '${resourceKey}') {
    if (event.type === '${kind}Deleted') {
${deletedReturn}
    }
    if (event.type === '${kind}Created') {
${createdReturn}
    }
    if (event.type === '${kind}Updated') {
${updatedReturn}
    }
  }`
    })
    .join('\n')

  return `${GENERATED_HEADER}import type { ClusterEvent } from '../../../cluster/events/types'
import type { Resource } from '../../../kubectl/commands/types'

export const extractMetaFromGeneratedClusterEvent = (
  event: ClusterEvent,
  parsedResource: Resource
): { name: string; namespace: string; labels?: Record<string, string> } | undefined => {
${resourceBlocks}
  return undefined
}
`
}

const main = (): void => {
  const raw = fs.readFileSync(manifestPath, 'utf8')
  const parsed = YAML.parse(raw) as ClusterResourcesManifest
  const structural = validateClusterResourcesManifest(parsed)
  if (!structural.ok) {
    console.error('clusterResources.manifest.yaml validation failed:')
    for (const e of structural.errors) {
      console.error(`  - ${e}`)
    }
    process.exit(1)
  }

  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true })
  }
  const yamlParsersDir = path.dirname(yamlParsersOut)
  if (!fs.existsSync(yamlParsersDir)) {
    fs.mkdirSync(yamlParsersDir, { recursive: true })
  }
  const getResourceHandlerDir = path.dirname(getResourceHandlerRegistryOut)
  if (!fs.existsSync(getResourceHandlerDir)) {
    fs.mkdirSync(getResourceHandlerDir, { recursive: true })
  }

  fs.writeFileSync(resourceModelsOut, buildResourceModelsFile(parsed), 'utf8')
  fs.writeFileSync(typesOut, buildTypesFile(parsed), 'utf8')
  fs.writeFileSync(registryOut, buildRegistryFile(parsed), 'utf8')
  fs.writeFileSync(resourceCatalogOut, buildResourceCatalogFile(parsed), 'utf8')
  fs.writeFileSync(yamlParsersOut, buildYamlParsersFile(parsed), 'utf8')
  fs.writeFileSync(
    getResourceHandlerRegistryOut,
    buildGetResourceHandlerRegistryFile(parsed),
    'utf8'
  )
  fs.writeFileSync(
    clusterEventTypesOut,
    buildClusterEventTypesFile(parsed),
    'utf8'
  )
  fs.writeFileSync(
    clusterEventWatchMetaOut,
    buildClusterEventWatchMetaFile(parsed),
    'utf8'
  )
  console.log(`Wrote ${path.relative(workspaceRoot, resourceModelsOut)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, typesOut)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, registryOut)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, resourceCatalogOut)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, yamlParsersOut)}`)
  console.log(
    `Wrote ${path.relative(workspaceRoot, getResourceHandlerRegistryOut)}`
  )
  console.log(`Wrote ${path.relative(workspaceRoot, clusterEventTypesOut)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, clusterEventWatchMetaOut)}`)
}

main()

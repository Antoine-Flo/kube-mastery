import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import YAML from 'yaml'
import {
  buildKubectlAliasesForManifestEntry,
  validateClusterResourcesManifest,
  type ClusterResourcesManifest
} from '../src/core/cluster/validateClusterResourcesManifest'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const workspaceRoot = path.resolve(__dirname, '..')
const manifestPath = path.join(
  workspaceRoot,
  'src/core/cluster/clusterResources.manifest.yaml'
)
const outDir = path.join(workspaceRoot, 'src/core/cluster/generated')
const typesOut = path.join(outDir, 'clusterResourceTypes.generated.ts')
const registryOut = path.join(outDir, 'clusterRegistry.generated.ts')
const resourceCatalogOut = path.join(
  workspaceRoot,
  'src/core/kubectl/commands/resourceCatalog.generated.ts'
)

const GENERATED_HEADER = `// Code generated by bin/generate-cluster-resources.ts. DO NOT EDIT.

`

const buildTypesFile = (manifest: ClusterResourcesManifest): string => {
  const imports = new Set<string>()
  imports.add(
    "import type { ResourceCollection } from '../repositories/types'"
  )
  for (const r of manifest.resources) {
    imports.add(
      `import type { ${r.typeName} } from '${r.importPath.replace(/^\./, '..')}'`
    )
  }
  const importLines = [...imports].sort().join('\n')

  const dataFields = manifest.resources
    .map(
      (r) =>
        `  ${r.collectionKey}: ResourceCollection<${r.typeName}>`
    )
    .join('\n')

  const byKindFields = manifest.resources
    .map((r) => `  ${r.kind}: ${r.typeName}`)
    .join('\n')

  const keysTuple = manifest.resources
    .map((r) => `  '${r.collectionKey}'`)
    .join(',\n')

  return `${GENERATED_HEADER}${importLines}

export interface ClusterStateData {
${dataFields}
}

type ResourceByKind = {
${byKindFields}
}

export type ResourceKind = keyof ResourceByKind

export type KindToResource<TKind extends ResourceKind> = ResourceByKind[TKind]

export type ClusterStateCollectionsInput = Partial<{
  [K in keyof ClusterStateData]: ClusterStateData[K] extends ResourceCollection<
    infer U
  >
    ? U[]
    : never
}>

export const CLUSTER_DATA_KEYS = [
${keysTuple}
] as const satisfies readonly (keyof ClusterStateData)[]
`
}

const buildRegistryFile = (manifest: ClusterResourcesManifest): string => {
  const typeImports = manifest.resources
    .map((r) => `import type { ${r.typeName} } from '${r.importPath.replace(/^\./, '..')}'`)
    .filter((line, i, arr) => arr.indexOf(line) === i)
    .sort()
    .join('\n')

  const eventImports = manifest.resources
    .flatMap((r) => {
      const k = r.kind
      return [
        `create${k}CreatedEvent`,
        `create${k}DeletedEvent`,
        `create${k}UpdatedEvent`
      ]
    })
    .filter((name, i, arr) => arr.indexOf(name) === i)
    .sort()
    .join(',\n  ')

  const reposBody = manifest.resources
    .map(
      (r) =>
        `  ${r.collectionKey}: createResourceRepository<${r.typeName}>('${r.repoKindArg}')`
    )
    .join(',\n')

  const opsBody = manifest.resources
    .map(
      (r) =>
        `  ${r.collectionKey}: createResourceOperations<${r.typeName}>(repos.${r.collectionKey}, '${r.collectionKey}')`
    )
    .join(',\n')

  const factoriesBody = manifest.resources
    .map(
      (r) => `  ${r.kind}: {
    created: create${r.kind}CreatedEvent,
    deleted: create${r.kind}DeletedEvent,
    updated: create${r.kind}UpdatedEvent
  }`
    )
    .join(',\n')

  return `${GENERATED_HEADER}import { createResourceRepository } from '../repositories/resourceRepository'
import { createResourceOperations } from '../resourceStateOps'
${typeImports}
import {
  ${eventImports}
} from '../events/types'

export const repos = {
${reposBody}
} as const

export const resourceOps = {
${opsBody}
}

export const EVENT_FACTORIES = {
${factoriesBody}
} as const

export type EventFactoryKind = keyof typeof EVENT_FACTORIES
`
}

const buildKubectlResourceEntries = (
  manifest: ClusterResourcesManifest
): string[] => {
  const entries: string[] = []
  const extras = manifest.kubectlExtraResources ?? []
  const extraByCanon = new Map(
    extras.map((e) => [e.canonical, e] as const)
  )
  const emitExtra = (canonical: string): void => {
    const e = extraByCanon.get(canonical)
    if (!e) {
      return
    }
    entries.push(`  ${canonical}: ${JSON.stringify(e.aliases)},`)
  }
  emitExtra('all')
  for (const r of manifest.resources) {
    const key = r.collectionKey.toLowerCase()
    const aliases = buildKubectlAliasesForManifestEntry(r)
    entries.push(`  ${key}: ${JSON.stringify(aliases)},`)
  }
  for (const e of extras) {
    if (e.canonical === 'all') {
      continue
    }
    entries.push(`  ${e.canonical}: ${JSON.stringify(e.aliases)},`)
  }
  return entries
}

const deriveKubectlSingularApiReference = (
  kind: string,
  apiVersion: string
): string => {
  if (apiVersion === 'v1') {
    return kind.toLowerCase()
  }
  const slash = apiVersion.indexOf('/')
  if (slash === -1) {
    return kind.toLowerCase()
  }
  const group = apiVersion.slice(0, slash)
  return `${kind.toLowerCase()}.${group}`
}

const deriveKubectlPluralApiReference = (
  facadePlural: string,
  apiVersion: string
): string => {
  const plural = facadePlural.toLowerCase()
  if (apiVersion === 'v1') {
    return plural
  }
  const slash = apiVersion.indexOf('/')
  if (slash === -1) {
    return plural
  }
  const group = apiVersion.slice(0, slash)
  return `${plural}.${group}`
}

const groupFromOutputApiVersion = (apiVersion: string): string => {
  if (apiVersion === 'v1') {
    return ''
  }
  const idx = apiVersion.indexOf('/')
  if (idx === -1) {
    return ''
  }
  return apiVersion.slice(0, idx)
}

const deriveExtraDiscoveryRefs = (
  canonical: string,
  outputKind: string,
  outputApiVersion: string
): { singular: string; plural: string } => {
  const group = groupFromOutputApiVersion(outputApiVersion)
  const singularBase = outputKind.toLowerCase()
  return {
    singular: `${singularBase}.${group}`,
    plural: `${canonical}.${group}`
  }
}

const isNamespacedFacade = (facade: string): boolean => {
  return facade === 'standardNamespaced'
}

const buildResourceCatalogFile = (manifest: ClusterResourcesManifest): string => {
  const supportedKinds = manifest.resources.map((r) => r.kind)
  const namespacedKinds = manifest.resources
    .filter((r) => isNamespacedFacade(r.facade))
    .map((r) => r.kind)

  const kindRefEntries = manifest.resources.map((r) => {
    const ref = deriveKubectlSingularApiReference(r.kind, r.apiVersion)
    return `  ${r.kind}: ${JSON.stringify(ref)}`
  })
  const pluralRefEntries = manifest.resources.map((r) => {
    const ref = deriveKubectlPluralApiReference(r.facadePlural, r.apiVersion)
    return `  ${r.kind}: ${JSON.stringify(ref)}`
  })

  const extras = manifest.kubectlExtraResources ?? []
  const rkLines: string[] = ['  all: undefined,']
  for (const r of manifest.resources) {
    rkLines.push(
      `  ${r.collectionKey.toLowerCase()}: ${JSON.stringify(r.kind)},`
    )
  }
  for (const e of extras) {
    if (e.canonical === 'all') {
      continue
    }
    rkLines.push(`  ${e.canonical}: undefined,`)
  }

  const omLines: string[] = []
  for (const r of manifest.resources) {
    omLines.push(
      `  ${r.collectionKey.toLowerCase()}: { apiVersion: ${JSON.stringify(r.apiVersion)}, kind: ${JSON.stringify(r.kind)} },`
    )
  }
  for (const e of extras) {
    if (e.canonical === 'all') {
      continue
    }
    omLines.push(
      `  ${e.canonical}: { apiVersion: ${JSON.stringify(e.outputApiVersion ?? '')}, kind: ${JSON.stringify(e.outputKind ?? '')} },`
    )
  }

  const discoveryLines: string[] = []
  for (const e of extras) {
    if (e.canonical === 'all') {
      continue
    }
    if (!e.outputKind || !e.outputApiVersion) {
      continue
    }
    const refs = deriveExtraDiscoveryRefs(
      e.canonical,
      e.outputKind,
      e.outputApiVersion
    )
    discoveryLines.push(
      `  ${e.canonical}: { singular: ${JSON.stringify(refs.singular)}, plural: ${JSON.stringify(refs.plural)} },`
    )
  }

  const supportedBody = supportedKinds
    .map((k) => `  ${JSON.stringify(k)}`)
    .join(',\n')
  const namespacedBody = namespacedKinds
    .map((k) => `  ${JSON.stringify(k)}`)
    .join(',\n')
  const kubectlResourceEntries = buildKubectlResourceEntries(manifest)

  return `${GENERATED_HEADER}import type { ResourceKind } from '../../cluster/generated/clusterResourceTypes.generated'

// Kubectl resources: canonical name -> list of aliases
export const KUBECTL_RESOURCES = {
${kubectlResourceEntries.join('\n')}
} as const

export type KubectlResource = keyof typeof KUBECTL_RESOURCES

export const SUPPORTED_RESOURCE_KINDS: ResourceKind[] = [
${supportedBody}
]

export const NAMESPACED_RESOURCE_KINDS: ResourceKind[] = [
${namespacedBody}
]

export const RESOURCE_KIND_BY_RESOURCE: Record<
  KubectlResource,
  ResourceKind | undefined
> = {
${rkLines.join('\n')}
}

export const KIND_REFERENCE_BY_KIND: Record<ResourceKind, string> = {
${kindRefEntries.join(',\n')}
}

export const PLURAL_KIND_REFERENCE_BY_KIND: Record<ResourceKind, string> = {
${pluralRefEntries.join(',\n')}
}

export const RESOURCE_OUTPUT_METADATA_BY_RESOURCE: Record<
  Exclude<KubectlResource, 'all'>,
  { apiVersion: string; kind: string }
> = {
${omLines.join('\n')}
}

export const RESOURCE_DISCOVERY_REF_BY_RESOURCE: Partial<
  Record<KubectlResource, { singular: string; plural: string }>
> = {
${discoveryLines.join('\n')}
}
`
}

const main = (): void => {
  const raw = fs.readFileSync(manifestPath, 'utf8')
  const parsed = YAML.parse(raw) as ClusterResourcesManifest
  const structural = validateClusterResourcesManifest(parsed)
  if (!structural.ok) {
    console.error('clusterResources.manifest.yaml validation failed:')
    for (const e of structural.errors) {
      console.error(`  - ${e}`)
    }
    process.exit(1)
  }

  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true })
  }

  fs.writeFileSync(typesOut, buildTypesFile(parsed), 'utf8')
  fs.writeFileSync(registryOut, buildRegistryFile(parsed), 'utf8')
  fs.writeFileSync(resourceCatalogOut, buildResourceCatalogFile(parsed), 'utf8')
  console.log(`Wrote ${path.relative(workspaceRoot, typesOut)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, registryOut)}`)
  console.log(`Wrote ${path.relative(workspaceRoot, resourceCatalogOut)}`)
}

main()

const buildResourceModelsFile = (
  manifest: ClusterResourcesManifest
): string => {
  const byKindBody = manifest.resources
    .map((resource) => {
      return `  ${resource.kind}: import('${resource.importPath.replace(/^\./, '..')}').${resource.typeName}`
    })
    .join('\n')

  const byCollectionKeyBody = manifest.resources
    .map((resource) => {
      return `  ${resource.collectionKey}: ClusterResourceTypeByKind['${resource.kind}']`
    })
    .join('\n')

  return `${GENERATED_HEADER}export type ClusterResourceTypeByKind = {
${byKindBody}
}

export type ClusterResourceTypeByCollectionKey = {
${byCollectionKeyBody}
}
`
}

